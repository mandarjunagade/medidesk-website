                    <div class="blog-faq">
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Was ist digitale Patientenkommunikation?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>Digitale Patientenkommunikation bezeichnet alle elektronischen Kanäle, über die Arztpraxen mit ihren Patient:innen kommunizieren: Online-Rezeptionen, E-Mail, Patientenportale, SMS-Erinnerungen, Videosprechstunden und Chat-Widgets. Ziel ist 24/7-Erreichbarkeit, Telefonentlastung und DSGVO-konforme Strukturierung aller Anfragen.</p></div>
                        </div>
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Welche digitalen Kommunikationskanäle sind DSGVO-konform?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>DSGVO-konforme Kanäle: Online-Rezeptionen mit EU-Hosting und AVV (MediDesk, 321 MED), KIM (TI-basiert), PVS-integrierte Patientenportale, zertifizierte Apps (Siilo), Videosprechstunde (KBV-zugelassen). WhatsApp und normale E-Mail sind NICHT zulässig für Gesundheitsdaten. Details: <a href="<?= $root; ?>blog/sichere-patientenkommunikation-alternativen/">DSGVO-konforme Alternativen</a>.</p></div>
                        </div>
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Müssen Patienten der digitalen Kommunikation zustimmen?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>Ja, für die Verarbeitung von Gesundheitsdaten ist nach DSGVO Art. 9 eine ausdrückliche Einwilligung erforderlich. Bei Online-Rezeptionen mit EU-Hosting ist die Einwilligung beim Absenden des Formulars integriert. Telefonkommunikation benötigt keine separate Einwilligung.</p></div>
                        </div>
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Wie bindet man ältere Patienten in die digitale Kommunikation ein?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>Durch persönliche Einführung beim Praxisbesuch, einfache Formulare ohne Login-Pflicht und die Beibehaltung des Telefonkanals parallel. 72% der über 65-Jährigen nutzen Smartphones (BITKOM, 2024). Wichtig: Das Telefon wird ergänzt, nicht abgeschafft.</p></div>
                        </div>
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Was ist der Unterschied zwischen Online-Rezeption und Patientenportal?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>Eine Online-Rezeption nimmt Anfragen ohne Login entgegen – höhere Nutzungsrate, sofortiger Start. Ein Patientenportal erfordert Registrierung, bietet dafür Befundeinsicht und Dokumentenaustausch. Für die meisten Praxen ist eine Online-Rezeption der sinnvollere erste Schritt.</p></div>
