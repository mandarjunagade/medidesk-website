                            <tbody>
                                <tr>
                                    <td><strong>EU-Hosting</strong></td>
                                    <td>Daten dürfen nur auf Servern in der EU gespeichert werden. US-Cloud-Dienste (WhatsApp, Google Forms) sind unzulässig für Gesundheitsdaten.</td>
                                    <td>Bußgeld bis 4% des Jahresumsatzes</td>
                                </tr>
                                <tr>
                                    <td><strong>Auftragsverarbeitungsvertrag (AVV)</strong></td>
