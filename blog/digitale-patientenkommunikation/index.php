                    <h2 class="blog-section-title">2. DSGVO-Anforderungen: Was erlaubt ist und was nicht</h2>

                    <p>Gesundheitsdaten sind nach DSGVO Art. 9 besonders schützenswerte Daten. Für deren Verarbeitung gelten strengste Anforderungen. Verstöße werden von deutschen Datenschutzbehörden aktiv verfolgt – Bußgelder in sechsstelliger Höhe sind möglich. Unser Artikel zu <a href="<?= $root; ?>blog/sichere-patientenkommunikation-alternativen/">DSGVO-konformen WhatsApp-Alternativen</a> geht ins Detail.</p>

