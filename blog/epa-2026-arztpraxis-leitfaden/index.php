                            <div class="blog-faq-answer"><p>Die ePA ist ein digitales Gesundheitsprotokoll für alle GKV-Versicherten. Seit Januar 2025 erhalten alle Versicherten automatisch eine ePA (Opt-out). Arztpraxen sind verpflichtet, Befunde, Medikationspläne und E-Rezepte über die Telematikinfrastruktur einzupflegen.</p></div>
                        </div>
                        <div class="blog-faq-item">
                            <div class="blog-faq-question"><span>Was sind die Pflichten von Arztpraxen bezüglich der ePA 2026?</span><i class="fa-solid fa-chevron-down"></i></div>
                            <div class="blog-faq-answer"><p>Ab 2026 sind Arztpraxen verpflichtet: eArztbriefe per KIM zu versenden, Befunde und Laborergebnisse in die ePA einzustellen, E-Rezepte über die TI auszustellen (seit 2024 bereits Pflicht) und Medikationspläne bei Polymedikation zu pflegen. Verstöße können zu KV-Honorarkürzungen führen.</p></div>
